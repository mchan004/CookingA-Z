                    <h2>Cách làm món Sườn xào chua ngọt phủ phô mai</h2>
                    <p>Put the yeast in the bowl of a standing mixer. Add 1 2/3 cups/ 365 grams lukewarm water and allow the yeast to activate, about 5 minutes. Add flour, 1 tablespoon/18 grams salt and malt powder and mix at low speed for 5 minutes using the dough hook. Cover the dough and allow to rise at room temperature for about 2 hours.</p>
                    <p>Punch the dough down and shape into a rough rectangle about 1 to 1 1/2 inches thick, and about 2 times longer than it is wide. If you are having trouble forming the dough, stretch it, wait for the gluten to relax and re-form.
    Cut the dough into 10 pieces of about 3 ounces each. Roll each into an 8-inch-long snakelike shape, tapering the dough at each end. Circle the dough around your hand, pinching the ends together and rolling under your palm once or twice to seal. Put the bagels on a Silpat or other nonstick baking sheet on top of a jellyroll pan. Cover well with plastic wrap and refrigerate for about 10 hours, or up to 24 hours.
    Heat the oven to 425 degrees. If you have a baking stone or brick you use for baking, put it on a rack near the bottom of the oven; it will retain heat and produce a crisper bagel. Bring a large pot of water to boil, add the remaining salt and the baking soda into the boiling water and remove the bagels from the refrigerator.</p>
                    <p>Carefully place just enough bagels into the pot to cover the surface of the water, making sure that there are no bagels resting on top of one another. Let them float on 1 side for 1 minute before flipping them to the other side for another minute or so. Remove the bagels and drain well on a cooling rack.</p>
                    <p>Pour poppy or sesame seeds into a bowl wider than the bagels. Working very quickly, remove the bagels one by one and dip them into the topping. Place them back on the Silpat-covered baking sheet, topping side down.
    Bake on the second to highest shelf of the oven for 8 to 10 minutes. Then flip the bagels, rotate the pan and continue baking for about 8 minutes, or until they are golden brown.</p>
